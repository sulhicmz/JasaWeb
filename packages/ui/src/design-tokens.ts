export type ThemeMode = 'light' | 'dark';

export class DesignTokenService {
  private static instance: DesignTokenService;
  private currentTheme: ThemeMode = 'light';

  private constructor() {}

  public static getInstance(): DesignTokenService {
    if (!DesignTokenService.instance) {
      DesignTokenService.instance = new DesignTokenService();
    }
    return DesignTokenService.instance;
  }

  public setTheme(theme: ThemeMode): void {
    this.currentTheme = theme;
  }

  public getTheme(): ThemeMode {
    return this.currentTheme;
  }

  public getTokens(): DesignTokens {
    return this.currentTheme === 'dark' ? darkTokens : lightTokens;
  }

  public getColor(
    category: keyof DesignTokens['colors'],
    shade: string
  ): string {
    const tokens = this.getTokens();
    const colorCategory = tokens.colors[category] as any;
    return colorCategory[shade] || colorCategory[500];
  }

  public getFontSize(
    size: keyof DesignTokens['typography']['fontSize']
  ): string {
    return this.getTokens().typography.fontSize[size];
  }

  public getSpacing(size: keyof DesignTokens['spacing']): string {
    return this.getTokens().spacing[size];
  }

  public getBorderRadius(size: keyof DesignTokens['borderRadius']): string {
    return this.getTokens().borderRadius[size];
  }

  public getShadow(size: keyof DesignTokens['shadows']): string {
    return this.getTokens().shadows[size];
  }

  public getBreakpoint(size: keyof DesignTokens['breakpoints']): string {
    return this.getTokens().breakpoints[size];
  }

  public getZIndex(level: keyof DesignTokens['zIndex']): number {
    return this.getTokens().zIndex[level];
  }

  // Generate CSS custom properties for use in stylesheets
  public generateCSSProperties(): Record<string, string> {
    const tokens = this.getTokens();
    const cssVars: Record<string, string> = {};

    // Generate color variables
    Object.entries(tokens.colors).forEach(([category, shades]) => {
      Object.entries(shades).forEach(([shade, value]) => {
        cssVars[`--color-${category}-${shade}`] = value;
      });
    });

    // Generate typography variables
    Object.entries(tokens.typography.fontSize).forEach(([size, value]) => {
      cssVars[`--font-size-${size}`] = value;
    });

    Object.entries(tokens.typography.fontWeight).forEach(([weight, value]) => {
      cssVars[`--font-weight-${weight}`] = value.toString();
    });

    // Generate spacing variables
    Object.entries(tokens.spacing).forEach(([size, value]) => {
      cssVars[`--spacing-${size}`] = value;
    });

    // Generate border radius variables
    Object.entries(tokens.borderRadius).forEach(([size, value]) => {
      cssVars[`--border-radius-${size}`] = value;
    });

    // Generate shadow variables
    Object.entries(tokens.shadows).forEach(([size, value]) => {
      cssVars[`--shadow-${size}`] = value;
    });

    return cssVars;
  }
}

